const Mint = () => {
  const [message, setMessage] = useState("");
  const [minting, setMinting] = useState(false);
  const [toAddress, setToAddress] = useState("");
  const [uri, setUri] = useState("");

  async function mintNFT(evt) {
    evt.preventDefault();
    const data = {
      price: 0.01,
      toAddress: toAddress,
      uri: uri,
    };
    console.log(data);
    //Mint logic

    try {
      // Request account access if needed
      await window.ethereum.request({ method: "eth_requestAccounts" });

      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();
      const contract = new ethers.Contract(contractAddress, abi, signer);

      setMinting(true);
      const tx = await contract.safeMint(data.toAddress, data.uri, {
        value: ethers.utils.parseEther("0.01"),
      });
      await tx.wait();
      setMessage("NFT minted successfully!");
    } catch (error) {
      console.error(error);
      setMessage("Minting failed. Please try again.");
    } finally {
      setMinting(false);
    }
  }

  return (
    <div className="flex flex-col items-center justify-center min-h-screen bg-gray-100 p-4">
      <form
        className="w-full max-w-md bg-white shadow-lg rounded-lg p-6 space-y-4"
        onSubmit={mintNFT}
      >
        <input
          type="text"
          placeholder="0.01 ether"
          name="price"
          disabled={true}
          className="w-full px-4 py-2 border border-gray-300 rounded-md bg-gray-100 cursor-not-allowed"
        />
        <input
          type="text"
          placeholder="To    0x4529D...12A8"
          name="toAddress"
          value={toAddress}
          onChange={(e) => setToAddress(e.target.value)}
          className="w-full px-4 py-2 border border-gray-300 rounded-md"
        />
        <input
          type="text"
          placeholder="URI"
          name="uri"
          value={uri}
          onChange={(e) => setUri(e.target.value)}
          className="w-full px-4 py-2 border border-gray-300 rounded-md"
        />
        <button
          type="submit"
          disabled={minting}
          className={`w-full px-4 py-2 rounded-md text-white font-semibold transition ${
            minting
              ? "bg-gray-500 cursor-not-allowed"
              : "bg-blue-500 hover:bg-blue-600"
          }`}
        >
          {minting ? "Minting..." : "Mint NFT"}
        </button>
        {message && (
          <p className="mt-4 text-center text-green-500 font-semibold">
            {message}
          </p>
        )}
      </form>
    </div>
  );
};

export default Mint;
